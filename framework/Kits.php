<?php
namespace MM;
class Kits{
    /**
     * 获取毫秒级别的时间戳
     */
    public static function getMillisecond()
    {
//获取毫秒的时间戳
        $time = explode ( " ", microtime () );
        $time = $time[1] . ($time[0] * 1000);
        $time2 = explode( ".", $time );
        $time = $time2[0];
        return $time;
    }
}